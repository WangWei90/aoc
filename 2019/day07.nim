func permutations(arr: array[5, int]): seq[array[5, int]] =
  var a = arr
  result.add(a)
  while nextPermutation(a):
    result.add(a)

proc step1(): int =
  for inputs in permutations([0, 1, 2, 3, 4]):
    var output = 0

    for i in inputs:
      var computer = init_computer(input, @[i, output])
      output = computer.process()[0]

    result = max(result, output)


proc step2(): int =
  for phases in permutations([5, 6, 7, 8, 9]):
    var
      computers = phases.mapIt(init_computer(input, @[it]))
      output = @[0]
      thruster_signal = 0

    for c in computers.mitems:
      discard c.process()

    while output.len > 0:
      for i, c in computers.mpairs:
        c.inputs = output.reversed()
        output = c.process()
        if output.len > 0:
          thruster_signal = output[^1]

    result = max(result, thruster_signal)


